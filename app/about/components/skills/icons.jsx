// Helper function to get icon class
const getTechIcon = (tech) => {
  return techIcons[tech] || "devicon-devicon-plain";
};

// Helper function to check if icon exists
const hasTechIcon = (tech) => {
  return techIcons.hasOwnProperty(tech);
};

// TechIcon component for rendering technology icons
const TechIcon = ({ tech, className = "text-4xl", showTooltip = true }) => {
  const iconClass = getTechIcon(tech);

  if (hasTechIcon(tech)) {
    return (
      <div className="group relative">
        <i className={`${iconClass} ${className}`}></i>
        {showTooltip && (
          <span className="text-xs text-foreground font-medium text-center opacity-0 group-hover:opacity-100 transition-opacity duration-200 absolute -bottom-6 bg-black/80 text-white px-2 py-1 rounded-md whitespace-nowrap">
            {tech}
          </span>
        )}
      </div>
    );
  }

  // Fallback: Show first letter or acronym in a colored box
  const getAcronym = (name) => {
    if (name.length <= 3) return name;
    return name
      .split(" ")
      .map((word) => word[0])
      .join("")
      .toUpperCase();
  };

  const getColor = (name) => {
    let hash = 0;
    for (let i = 0; i < name.length; i++) {
      hash = name.charCodeAt(i) + ((hash << 5) - hash);
    }
    const colors = [
      "bg-blue-500",
      "bg-green-500",
      "bg-yellow-500",
      "bg-red-500",
      "bg-purple-500",
      "bg-pink-500",
      "bg-indigo-500",
      "bg-teal-500",
    ];
    return colors[Math.abs(hash) % colors.length];
  };

  return (
    <div className="group relative">
      <div
        className={`${getColor(
          tech
        )} w-12 h-12 rounded-lg flex items-center justify-center text-white font-bold text-sm ${className}`}
      >
        {getAcronym(tech)}
      </div>
      {showTooltip && (
        <span className="text-xs text-foreground font-medium text-center opacity-0 group-hover:opacity-100 transition-opacity duration-200 absolute -bottom-6 bg-black/80 text-white px-2 py-1 rounded-md whitespace-nowrap">
          {tech}
        </span>
      )}
    </div>
  );
};

